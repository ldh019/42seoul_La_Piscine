char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	char				*ret;
	unsigned int		i;

	i = 0;
	ret = dest;
	while (*dest != '\0')
		dest++;
	while (*src != '\0' && i < nb)
	{
		*dest = *src;
		dest++;
		src++;
		i++;
	}
	*dest = '\0';
	return (ret);
}
